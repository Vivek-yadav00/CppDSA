#include <iostream>
using namespace std;
int main()
{
    int a;
    cin >> a;
    cout << "Operators in c++:\n";
    cout << "The value of a++ is " << a++ << "\n";
    cout << "The value of a is " << a << "\n";
    cout << "The value of a-- is " << a-- << "\n";
    cout << "The value of a is " << a << "\n";
    cout << "The value of ++a is " << ++a << "\n";
    cout << "The value of --a is " << --a << "\n";
    return 0;
}
